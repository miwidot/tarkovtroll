export function t(key) {
  let currentLocale
  locale.subscribe(v => currentLocale = v)()
  return translations[currentLocale]?.[key] || translations['de']?.[key] || key
}

export const i18n = derived(locale, ($locale) => {
  return (key) => translations[$locale]?.[key] || translations['de']?.[key] || key
})
